#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*ss;

	ss = (unsigned char *)s;
	i = 0;
	while (n--)
	{
		ss[i] = '\0';
		i++;
	}
	s = ss;
}
